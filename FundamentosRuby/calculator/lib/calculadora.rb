def soma(primeiro_numero, segundo_numero)
  primeiro_numero + segundo_numero
end

def subtracao(primeiro_numero, segundo_numero)
  primeiro_numero - segundo_numero
end

def multiplicacao(primeiro_numero, segundo_numero)
  primeiro_numero * segundo_numero
end

def divisao(x, y)
  y == 0 ? "Opa! Zero como divisor" : x / y
end
